#include "libft.h"

void	ft_putnbr(int n)
{
	long	n2;
	long	d;

	n2 = (long)n;
	d = 1;
	if (n2 < 0)
	{
		ft_putchar('-');
		n2 *= -1;
	}
	while (n2 >= 10 * d)
		d *= 10;
	while (d)
	{
		ft_putchar(ft_itoc(n2 / d));
		n2 %= d;
		d /= 10;
	}
}
