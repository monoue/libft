#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	len;

	len = ft_strlen(s);
	while (*s != '\0')
		s++;
	while (len-- > 0 && *s != c)
		s--;
	if (*s == c)
		return ((char *)s);
	else
		return (NULL);
}